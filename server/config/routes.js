var path = require("path");
var mongoose = require("mongoose");
var Pet = mongoose.model("Pet");
// var Skill = mongoose.model("Skill");
var pets = require("../controllers/pets");

module.exports = function (app) {

    ///////////////////////////////////////////////////////////////////////////
    //////////////////////////// Pet related routes ///////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    app.get("/pets", function(request, response){
        pets.getAllPets(request, response);
    });

    app.get("/pets/:id", function(request, response) {
        pets.getOnePet(request, response);
    });

    app.post("/pets", function (request, response) {
        pets.createOnePet(request, response);
    });

    app.put("/pets", function (request, response) {
        pets.updatePet(request, response);
    });

    app.delete("/pets/:id", function (request, response){
        pets.destroyPet(request, response);
    });


    ///////////////////////////////////////////////////////////////////////////
    ////////////////////////// Quote related routes ///////////////////////////
    ///////////////////////////////////////////////////////////////////////////










    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////// Angular related routes //////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    app.all("*", (request, response, next) => {
        response.sendFile(path.resolve("./../client/dist/index.html"));
    });

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////


}
